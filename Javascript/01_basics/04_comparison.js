console.log(null > 0);
console.log(null == 0);
console.log(null >= 0);

// null > 0 → null converts to 0, so 0 > 0 → false.
// null == 0 → null only equals undefined, so false.
// null >= 0 → null converts to 0, so 0 >= 0 → true.
// Comparison (>=, >, <, <=) converts null to a number, but == does not.


